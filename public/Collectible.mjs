import { dimension } from './dimension.mjs';

class Collectible {
  constructor({x, y, value, id}) {
    this.x = x;
    this.y = y;
    this.value = value;
    this.id = id;
    this.width = dimension.COIN_WIDTH;
    this.height = dimension.COIN_HEIGHT;
  }

}

